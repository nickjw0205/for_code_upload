package elice;

public class third_8 {
	public static void main(String args[]) {
		
		//적당한 3의 배수를 a,b,c에 넣어 주세요
		int a = 15;
		int b = 12;
		int c = 18;
		
		if(a%3 == 0){//3의 배수인가?
			System.out.println("a는 3의 배수");
		}else{
			System.out.println("a는 3의 배수 아님");
		}
		
		if(b%3 == 0){
			System.out.println("b는 3의 배수");	
		}else{
			System.out.println("b는 3의 배수 아님");
		}
		
		if(c%3 == 0){
			System.out.println("c는 3의 배수");
		}else{
			System.out.println("c는 3의 배수 아님");
		}
		
	}

}
