class UserInfo {
    private String userName;
    private int userAge;

    void setUserName(String userName) {
        this.userName = userName;
    }
    void setUserAge(int userAge) {
        this.userAge = userAge;
    }

    String getUserName() {
        return this.userName;
    }
    int getUserAge() {
        return this.userAge;
    }
}